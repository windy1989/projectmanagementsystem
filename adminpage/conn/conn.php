	function enkripsi($anu){
		if($anu == ''){
			$val = "";
		}else{
			$val = strrev(implode('-',str_split(str_replace('=','',base64_encode($anu)),5)));
		}
		
		return $val;
	}
	
	function dekripsi($una){
		$val = base64_decode(str_replace('-','',strrev($una)));
		return $val;
	}
	
	function tgl_indo($tanggal){
		$bulan = array (
			1 =>   'Januari',
			'Februari',
			'Maret',
			'April',
			'Mei',
			'Juni',
			'Juli',
			'Agustus',
			'September',
			'Oktober',
			'November',
